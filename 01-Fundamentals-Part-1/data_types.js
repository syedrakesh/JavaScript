let javascriptIsFun = true; // 'let' use for variable declaration.
console.log(javascriptIsFun);

var javascriptIsNotAmazing = false; // 'var' use for variable declaration.

javascriptIsFun = "YES!"; // In 'let' or 'var', variable value can be mutated. This is called variable mutation.
console.log(typeof javascriptIsFun);

console.log(typeof true);
console.log(typeof javascriptIsFun);
console.log(typeof 23);
console.log(typeof "Syed Rakesh");

let year;
console.log(year);
console.log(typeof year);

let month = null;
console.log(typeof month);

console.log(typeof null);

const birthYear = 1997; // 'const' variable value can not be changed.
console.log(birthYear);
